ll fastpow (ll x, ll y) {
    ll exponent=y, ret=1;
    while (exponent) {
        if (exponent%2) {
            ret*=x;
            ret%=MOD;
        }
        x*=x;
        x%=MOD;
        exponent/=2;
    }
    return ret;
}
vector<int> isprime, primes, minfactor;
void linear_sieve(int sz) {
    for (int i=0; i<=sz; i++) {
        isprime.push_back(1);
        minfactor.push_back(i);
    }
    for (int i=2; i<=sz; i++) {
        if (isprime[i]) primes.push_back(i);
        for (int j: primes) {
            if (i*j>sz) break;
            isprime[i*j]=0;
            minfactor[i*j]=j;
            if (i%j==0) break;
        }
    }
}

void solve() {
    int n; cin >> n;
    vector<int> a(n+1);
    for (int i=1; i<=n; i++) cin >> a[i];
    
    ll ans=0;
    vector<ll> hash(n*n+n+10, 0);
    for (int j=n; j>=1; j--) {
        int k=j+1;
        for (int l=k+1; l<=n; l++) {
            int cur=a[k]*n+a[l];
            hash[cur]++;
        }
        for (int i=1; i<j; i++) {
            int cur=a[i]*n+a[j];
            ans+=hash[cur];
        }
    }
    cout << ans << "\n";
}

